export function getRouteLandingPage(slug) {
  return routeLandingPages.find((page) => page.slug === slug);
}

export function routeQuoteSearch(page) {
  const params = new URLSearchParams({
    pol: page.origin,
    pod: page.destination,
    lane: page.lane,
    containerType: "LCL",
    serviceScope: "door_to_door",
    cargo: page.ctaCargo,
  });

  return `?${params.toString()}`;
}
